package com.polban.jtk.JadwalAkademik;

public class Person {
    private String nama;
    private String prodi;

    public Person(String nama, String prodi) {
        this.nama = nama;
        this.prodi = prodi;
    }

    public String getNama() {
        return nama;
    }

    public String getProdi() {
        return prodi;
    }
}

